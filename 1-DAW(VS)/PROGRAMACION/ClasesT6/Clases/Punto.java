package ClasesT6.Clases;

public class Punto {

    // Atributos
    
    private float x;
    private float y;


    // Constructores

    public Punto(float x, float y){

        this.x = x;
        this.y = y;
    }

    // Metodos

    public float getX() {
        return x;
    }


    public void setX(int x) {
        this.x = x;
    }


    public float getY() {
        return y;
    }


    public void setY(int y) {
        this.y = y;
    }

    
}
